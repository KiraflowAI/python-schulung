kunden = 0
while not kunden == 19:
    print(kunden)
    kunden = kunden + 5

    if kunden >= 12:
        kunden = kunden - 7

# In jedem Schleifendurchlauf soll kunden um 5 wachsen.
# Wenn kunden dann größer als 12 ist, sollen 7 abgezogen werden.
# Wenn kunden gleich 19 ist, soll die Schleife enden.


