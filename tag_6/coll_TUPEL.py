# In Python ist ein Tupel eine grundlegende Datenstruktur, die einer Liste ähnlich ist, aber einen entscheidenden Unterschied aufweist: Sie ist unveränderlich.

# Das bedeutet, dass die vom Tupel referenzierten Objekte nicht mehr geändert werden können. Dies ist nützlich, um die Integrität der Daten im gesamten Programm zu gewährleisten.

# Tupel werden definiert, indem Elemente in Klammern ( ) eingeschlossen werden, wobei die Elemente durch Kommas getrennt sind.

# Ein einfaches Beispiel für ein Tupel zum Beispiel die Definition von Koordinaten als ein Paar von zwei Fließkommazahlen:


treasure_coordinates = (50.8215, -0.1372)
print(treasure_coordinates)



